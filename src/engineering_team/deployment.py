"""Production asset generation utilities for AutoDev Crew.

AutoDev Crew adds deployment-readiness to every generated project run. The assets
created here are deterministic templates that wrap the generated Python module,
Gradio app, and tests with Docker, CI, environment, and deployment guidance.
"""

from __future__ import annotations

import json
import os
import platform
import zipfile
from datetime import datetime
from pathlib import Path
from typing import Any, Dict, List


def _write_text(path: Path, content: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(content.strip() + "\n", encoding="utf-8")


def _safe_name(value: str, fallback: str = "generated-project") -> str:
    value = value.strip().lower().replace("_", "-")
    cleaned = "".join(ch if ch.isalnum() or ch == "-" else "-" for ch in value)
    cleaned = "-".join(part for part in cleaned.split("-") if part)
    return cleaned or fallback


def _dockerfile(inputs: Dict[str, Any]) -> str:
    app_file = inputs.get("app_file", "app.py")
    return f"""
# Auto-generated by AutoDev Crew
FROM python:3.11-slim

ENV PYTHONDONTWRITEBYTECODE=1 \\
    PYTHONUNBUFFERED=1 \\
    GRADIO_SERVER_NAME=0.0.0.0 \\
    GRADIO_SERVER_PORT=7860

WORKDIR /app

RUN apt-get update \\
    && apt-get install -y --no-install-recommends build-essential curl \\
    && rm -rf /var/lib/apt/lists/*

COPY requirements.generated.txt ./requirements.generated.txt
RUN pip install --no-cache-dir -r requirements.generated.txt

COPY . .

EXPOSE 7860

HEALTHCHECK --interval=30s --timeout=10s --start-period=20s --retries=3 \\
    CMD python -c "import urllib.request; urllib.request.urlopen('http://127.0.0.1:7860', timeout=5)" || exit 1

CMD ["python", "{app_file}"]
"""


def _docker_compose(inputs: Dict[str, Any]) -> str:
    service_name = _safe_name(inputs.get("project_slug", "autodev-generated-project"))
    return f"""
# Auto-generated by AutoDev Crew
services:
  {service_name}:
    build: .
    container_name: {service_name}
    ports:
      - "7860:7860"
    env_file:
      - .env.example
    restart: unless-stopped
"""


