def _production_manifest(inputs: Dict[str, Any], generated_files: List[str]) -> str:
    payload = {
        "project_name": inputs.get("project_name"),
        "project_slug": inputs.get("project_slug"),
        "run_id": inputs.get("run_id"),
        "workflow_version": "1.0.0",
        "generated_at": datetime.now().isoformat(timespec="seconds"),
        "python_version": platform.python_version(),
        "platform": platform.platform(),
        "frontend_framework": "Gradio",
        "llm_model": "openai/gpt-4o",
        "deployment_assets": [
            "Dockerfile",
            "docker-compose.yml",
            ".dockerignore",
            "requirements.generated.txt",
            ".env.example",
            ".github/workflows/generated-project-ci.yml",
            "DEPLOYMENT.md",
            "production_manifest.json",
        ],
        "generated_project_files": generated_files,
        "recommended_local_commands": [
            "python -m pip install -r requirements.generated.txt",
            f"python {inputs.get('app_file', 'app.py')}",
            f"python -m unittest {inputs.get('test_file', 'test_generated_app.py')}",
            "docker compose up --build",
        ],
    }
    return json.dumps(payload, indent=2, ensure_ascii=False)


def create_run_zip(run_dir: Path) -> Path:
    """Create or refresh a ZIP archive for one generated run folder."""
    zip_path = run_dir.with_suffix(".zip")
    if zip_path.exists():
        zip_path.unlink()
    with zipfile.ZipFile(zip_path, "w", compression=zipfile.ZIP_DEFLATED) as archive:
        for file_path in sorted(run_dir.rglob("*")):
            if file_path.is_file():
                archive.write(file_path, arcname=file_path.relative_to(run_dir.parent))
    return zip_path


def generate_deployment_assets(inputs: Dict[str, Any]) -> Dict[str, Any]:
    """Generate deployment and CI/CD assets inside a run output folder."""
    run_dir = Path(inputs["run_output_dir"])
    run_dir.mkdir(parents=True, exist_ok=True)

    assets = {
        "Dockerfile": _dockerfile(inputs),
        "docker-compose.yml": _docker_compose(inputs),
        ".dockerignore": _dockerignore(),
        "requirements.generated.txt": _requirements_generated(),
        ".env.example": _env_example(inputs),
        ".github/workflows/generated-project-ci.yml": _github_actions(inputs),
        "DEPLOYMENT.md": _deployment_guide(inputs),
    }

    for relative_name, content in assets.items():
        _write_text(run_dir / relative_name, content)

    generated_files = [str(path.relative_to(run_dir)) for path in sorted(run_dir.rglob("*")) if path.is_file()]
    _write_text(run_dir / "production_manifest.json", _production_manifest(inputs, generated_files))
    generated_files = [str(path.relative_to(run_dir)) for path in sorted(run_dir.rglob("*")) if path.is_file()]
    zip_path = create_run_zip(run_dir)

    return {
        "deployment_assets_generated": True,
        "deployment_assets_completed_at": datetime.now().isoformat(timespec="seconds"),
        "deployment_assets": sorted(assets.keys()) + ["production_manifest.json"],
        "download_zip": str(zip_path),
        "generated_file_count": len(generated_files),
    }
